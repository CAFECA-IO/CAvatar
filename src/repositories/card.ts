const FAILURE_PENALTY = 1;

// Info: (20251027 - Luphia) 定義效果型別
export interface CardEffect {
  credit?: number[];
  stamina?: number[];
  resources?: number[];
}

export interface EventActions {
  id: number;
  event: string[];
  action: string[][];
  /** Info: (20251027 - Luphia)
   * result[甲乙][欺騙/合作][失敗/成功]
   * 例如 result[0][0][0] 甲方欺騙失敗描述
   *     result[0][0][1] 甲方欺騙成功描述
   */
  result: string[][][];
  /** Info: (20251027 - Luphia)
   * response[甲乙][欺騙/合作][失敗/成功]
   * 例如 response[1][1][0] 乙方合作失敗感嘆詞
   *     response[1][1][1] 乙方合作成功感嘆詞
   */
  response: string[][][];
}

export interface Card {
  id: number;
  parentId?: number;
  finish: boolean;
  description: string;
  image: string;
  leftMessage: string;
  leftEffect: CardEffect;
  rightMessage: string;
  rightEffect: CardEffect;
}

const leftEffect = (success = true) => {
  const effect = {
    credit: success ? [25, 25] : [10, 10],
    resources: success ? [25, 25] : [0, -20 * FAILURE_PENALTY]
  };
  return effect;
}

const rightEffect = (success = true) => {
  const effect = {
    credit: success ? [-25, -25] : [-10, -10 * FAILURE_PENALTY],
    resources: success ? [50, 25] : [0, 0]
  };
  return effect;
}

